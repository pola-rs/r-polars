#' @export
`$.PlRExpr` <- function(x, name) {
  member_names <- ls(x, all.names = TRUE)
  method_names <- names(PlRExpr)

  if (name %in% member_names) {
    env_get(x, name)
  } else if (name %in% method_names) {
    fn <- PlRExpr[[name]]
    self <- x$.ptr
    environment(fn) <- environment()
    fn
  } else {
    NextMethod()
  }
}
